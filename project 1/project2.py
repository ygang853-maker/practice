# 1. 입력 받기
# input() 함수는 사용자에게 값을 입력받은 함수
birth_year = int(input("태어난 연도를 입력하세요 (예: 2003): "))

# 2. 계산하기
# 현재 년도에서 태어난 연도를 빼서 나이 계산
# 한국식 나이 계산: (현재연도 - 태어난 연도) + 1
this_year = 2026
age = this_year - birth_year + 1

# 3. 결과 출력하기
# 문자와 숫자를 같이 출력할 때 f-string(f"...") 사용
print(f"당신은 {birth_year}에 태어났습니다.")
print(f"2026년 기준 당신의 한국식 나이는 {age}살입니다.")

# 4. 나머지 연산자 테스트
# 나이를 2로 나누었을 떄 나머지가 0이면 짝수, 1이면 홀수
remain = age % 2
print(f"나이를 2로 나눈 나머지: {remain}")